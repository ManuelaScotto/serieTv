import axios from 'axios'
const baseUrl = 'http://localhost:3001/series'

// assegniamo la promise direttamente alla request e chiamiamo il metodo then 

///////////////////////////////////////////////////////////////////////////////////
/*LETTURA metodo GET*/
///////////////////////////////////////////////////////////////////////////////////

const getAll = () => {
    const request = axios.get(baseUrl)
    return request.then(response => response.data)
}

///////////////////////////////////////////////////////////////////////////////////
/*AGGIUNTA metodo POST*/
///////////////////////////////////////////////////////////////////////////////////

const create = newObject => {
    const request = axios.post(baseUrl, newObject)
    return request.then(response => response.data)
}

///////////////////////////////////////////////////////////////////////////////////
/*MODIFICA metodo PUT*/
///////////////////////////////////////////////////////////////////////////////////

const update = (id, newObject) => {
    const request = axios.put(` ${baseUrl}/${id}`, newObject)
    return request.then(response => response.data)
}

///////////////////////////////////////////////////////////////////////////////////
/*CANCELLA metodo DELETE*/
///////////////////////////////////////////////////////////////////////////////////

const deleteSerie = id => {
    const request = axios.delete(` ${baseUrl}/${id}`)
    return request.then(response => response.data)
}

///////////////////////////////////////////////////////////////////////////////////
// il modulo SERIESERVICE restituisce un oggetto con quattro funzioni e ogni funzione restituisce una promise dal metodo axios
///////////////////////////////////////////////////////////////////////////////////
export default { getAll, create, update, deleteSerie }
